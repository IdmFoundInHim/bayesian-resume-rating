def filter_big_ten_conference_games(
    games: list[tuple[str, str]]
) -> list[tuple[str, str]]:
    in_conference_games = [
        (winner, loser)
        for winner, loser in games
        if winner in BIG_TEN_TEAMS and loser in BIG_TEN_TEAMS
    ]

    return in_conference_games


