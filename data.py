import csv


def process_csv(file_path):
    completed_games = []

    with open(file_path, mode="r") as file:
        reader = csv.reader(file)
        next(reader)  # Skip the header row if there is one
        for row in reader:
            game_complete, team_1_name, team_1_points, team_2_name, team_2_points = row
            if game_complete.lower() == "true":
                team_1_points = int(team_1_points)
                team_2_points = int(team_2_points)
                if team_1_points > team_2_points:
                    completed_games.append((team_1_name, team_2_name))
                else:
                    completed_games.append((team_2_name, team_1_name))

    return completed_games


